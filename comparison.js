//equel operator 
console.log(2 == 2); //true
console.log(2 == '2'); //true

//not equel operator 
console.log(2 != 2); //false
console.log(2 != '2'); //false

//strict equel operator
console.log(2 === 2); //true
console.log(2 === '2'); //false

//strict not equel operator
console.log(2 !== 2); //false
console.log(2 !== '2'); //true